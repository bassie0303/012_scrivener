#!/usr/bin/env python3
"""
問題を「分野」に分類する（弱点分野学習・網羅把握のため）。

行政書士試験は出題順がほぼ固定なので【番号レンジを主軸】にする:
  1-2 基礎法学 / 3-7 憲法 / 8-26 行政法 / 27-35 民法 / 36-40 商法・会社法
  41-43 多肢 / 44 記述(行政法) / 45-46 記述(民法) / 47-57 一般知識・基礎知識 / 58-60 文章理解

補正は【設問文(stem)の明示キーワードのみ】で行う（選択肢本文は他分野語が紛れて
誤発火するので使わない）。これで年度ごとの境界±1ずれや、基礎法学/憲法の切り分け、
多肢・一般知識の細分に対応する。

返す分野（トップレベル科目）:
  基礎法学 / 憲法 / 行政法 / 民法 / 商法・会社法 /
  政治・経済・社会 / 情報通信・個人情報保護 / 行政書士法等 / 文章理解
"""
import re

FIELDS = [
    "基礎法学", "憲法", "行政法", "民法", "商法・会社法",
    "政治・経済・社会", "情報通信・個人情報保護", "行政書士法等", "文章理解",
]

# 設問文に出る明示キーワード（強い順）。stem のみに適用する。
P_SHOHO = r"会社法|商法|発起人|設立時|取締役|監査役|株[券主]|交互計算|手形|小切手|商行為|商人|匿名組合|合資会社|合同会社"
P_GYOSEI = (r"行政手続法|行政不服審査|審査請求|再調査の請求|抗告訴訟|取消訴訟|差止め|無効等確認|"
            r"不作為の違法|国家賠償|損失補償|地方自治|地方公共団体|都道府県|市町村|条例|行政行為|"
            r"行政罰|行政上の|附款|教示|行政指導|行政立法|行政計画|情報公開|行政機関|処分性|原告適格|公権力")
P_MINPO = (r"民法|代理|無権代理|時効|抵当|質権|留置権|先取特権|相続|遺[産言]|債[権務]|連帯|保証|"
           r"不当利得|即時取得|占有|意思表示|錯誤|物権|不法行為|認知|親族|婚姻|離婚|消費貸借|"
           r"賃貸借|使用貸借|請負|委任|事務管理|契約不適合|抵当権|根抵当|地上権|相殺|債権者|売買契約")
P_KENPO = (r"憲法|基本的人権|表現の自由|報道|取材|法の下の平等|生存権|信教|政教分離|学問の自由|"
           r"財産権|国会|衆議院|参議院|内閣|司法権|裁判を受ける権利|違憲|選挙[権区]|統治|天皇|条約")
P_KISO = r"裁判員|法源|法令の形式|法格言|法の解釈|罪刑法定|基礎法学|私法と公法|法の効力|裁判制度"

# 一般知識・基礎知識(47-)の細分（stem 中心）
P_INFO = (r"個人情報|情報公開|情報通信|ディープフェイク|なりすまし|インターネット|デジタル|"
          r"電子[署政]|サイバー|ＡＩ|ＳＮＳ|通信の秘密|マイナンバー|プロバイダ|情報セキュリティ")
P_GYOSHO = r"行政書士法|戸籍法|住民基本台帳|公文書管理|地方公務員法|恩赦|諸法令"


def classify_field(q):
    n = q.get("number")
    stem = q.get("stem", "") or ""

    if n is None:
        return "その他"

    # 一般知識・基礎知識ゾーン
    if n >= 47:
        if n >= 58:
            return "文章理解"
        if re.search(P_INFO, stem):
            return "情報通信・個人情報保護"
        if re.search(P_GYOSHO, stem):
            return "行政書士法等"
        if re.search(r"文章理解|本文の趣旨|空欄に入る語|並べ替え", stem):
            return "文章理解"
        return "政治・経済・社会"

    # 多肢(41-43)・記述(44-46): 設問文のキーワードで憲法/行政法/民法を判定
    if 41 <= n <= 46:
        if re.search(P_SHOHO, stem):
            return "商法・会社法"
        if re.search(P_GYOSEI, stem):
            return "行政法"
        if re.search(P_MINPO, stem):
            return "民法"
        if re.search(P_KENPO, stem):
            return "憲法"
        return {41: "憲法", 42: "行政法", 43: "行政法", 44: "行政法", 45: "民法", 46: "民法"}[n]

    # 1-40: 番号レンジを土台に、設問文の明示法令だけで上書き（境界ずれ対応）
    base = _by_number(n)
    if re.search(P_SHOHO, stem):
        return "商法・会社法"
    if re.search(P_GYOSEI, stem):
        return "行政法"
    if re.search(P_MINPO, stem):
        return "民法"
    if re.search(P_KISO, stem):
        return "基礎法学"
    if re.search(P_KENPO, stem):
        return "憲法"
    return base


def _by_number(n):
    if n <= 2:
        return "基礎法学"
    if n <= 7:
        return "憲法"
    if n <= 26:
        return "行政法"
    if n <= 35:
        return "民法"
    if n <= 40:
        return "商法・会社法"
    return "その他"
